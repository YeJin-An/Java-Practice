class Ex2_5 {
   public static void main(String args[]) {
		
	   // 변수 + 리터럴
	   int score = 100;
	   score = 200;
	   System.out.println(score);

	   
	   // 상수 + 리터럴
	   final int content = 300;
	   System.out.println(content);
	   
	   final int hello;
	   hello = 400;
//	   hello = 500; 에러 발생
	   System.out.println(hello);
	   
	}
} 

